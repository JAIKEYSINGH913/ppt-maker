from __future__ import annotations

import logging
import re
import random
import os
from dataclasses import dataclass, field
from pathlib import Path
from io import BytesIO

from pptx import Presentation
from pptx.dml.color import RGBColor
from pptx.enum.shapes import MSO_SHAPE_TYPE, PP_PLACEHOLDER, MSO_AUTO_SHAPE_TYPE
from pptx.enum.text import PP_ALIGN, MSO_ANCHOR
from pptx.util import Inches, Pt
from pptx.chart.data import CategoryChartData
from pptx.enum.chart import XL_CHART_TYPE

from md2deck.config import AppConfig
from md2deck.models import PipelineArtifacts, SlideBlueprint, VisualIntent, LayoutMetadata
